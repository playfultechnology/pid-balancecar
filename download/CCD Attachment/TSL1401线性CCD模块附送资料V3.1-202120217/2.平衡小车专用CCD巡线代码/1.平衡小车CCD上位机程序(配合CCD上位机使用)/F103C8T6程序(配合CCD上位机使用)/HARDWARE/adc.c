/**************************************************************************
函数功能：CCD数据采集
入口参数：无
返回  值：无
**************************************************************************/
 void RD_TSL(void) 
{
  u8 i=0,tslp=0;
  TSL_CLK=1;
  TSL_SI=0; 
  Dly_us();
      
  TSL_SI=1; 
  TSL_CLK=0;
  Dly_us();
	
	
  TSL_CLK=1;
  TSL_SI=0;
  Dly_us(); 
  for(i=0;i<128;i++)					//读取128个像素点电压值
  { 
    TSL_CLK=0; 
    Dly_us();  //调节曝光时间
		Dly_us();

    ADV[tslp]=(Get_Adc(3))>>4;
    ++tslp;
    TSL_CLK=1;
     Dly_us();	
  }  
}
 
